# ==============================
# 工具描述的动态生成函数
# ==============================
def get_tools_description(tools):
    """
    根据传入的工具列表， 生成给LLM的工具描述字符串。
    """
    descriptions = []
    for tool in tools:
        desc = f"- {tool.name}: {tool.description}"
        descriptions.append(desc)
    return "\n".join(descriptions)

